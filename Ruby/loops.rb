# while condition is true the loop will continue to run
# WHILE loop
counter = 1
while counter < 11
  puts counter
  counter = counter + 1
end

i = 0
while i < 5
  puts i
  i += 1
end

# UNTIL loop

i = 0
until i == 6
  i = i + 1
end
puts i

counter = 1
until counter > 10
  puts counter
  counter += 1
end

